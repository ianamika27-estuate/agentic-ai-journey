def add_numbers(a, b):
    """Should return the sum of a and b."""
    return a + b

def reverse_string(s):
    """Should return the string reversed."""
    return s[::-1]

def is_even(n):
    """Should return True if n is even, False otherwise."""
    return n % 2 == 0

def subtract_numbers(a, b):
    """Should return the difference of a and b."""
    return a - b
